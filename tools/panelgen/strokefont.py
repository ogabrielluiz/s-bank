def text_width(s: str, size: float, tracking: float = 0.6) -> float:
    """Width (mm) of ``s`` at cap height ``size`` (mm)."""
    scale = size / 6.0
    return sum((_F.get(c, _F[" "])[0] + tracking) * scale for c in s.upper())


def text_paths(s: str, x: float, y: float, size: float, color: str,
               anchor: str = "start", weight: float = 0.18, tracking: float = 0.6) -> str:
    """SVG `<path>` markup drawing ``s`` as stroked vector glyphs.

    ``(x, y)`` is the baseline position; ``anchor`` ∈ {start, middle, end}.
    """
    scale = size / 6.0
    total = text_width(s, size, tracking)
    if anchor == "middle":
        x -= total / 2.0
    elif anchor == "end":
        x -= total
    top = y - size
    d_parts: list[str] = []
    cx = x
    for ch in s.upper():
        adv, polylines = _F.get(ch, _F[" "])
        for pl in polylines:
            pts = [(cx + px * scale, top + py * scale) for (px, py) in pl]
            d_parts.append("M" + " L".join(f"{px:.2f},{py:.2f}" for px, py in pts))
        cx += (adv + tracking) * scale
    if not d_parts:
        return ""
    return (f'<path d="{" ".join(d_parts)}" fill="none" stroke="{color}" '
            f'stroke-width="{weight:.2f}" stroke-linecap="round" stroke-linejoin="round"/>')
